                            reduced immunity. Intensity of symptoms depends upon worm load and its type. Infection due
                            to Liver fluke/Coccidiosis causes more damage when present. Diagnosis is done on the
                            examination of stools. There are specific effective drugs for the treatment. In broad
                            spectrum antithetical. ALBENDAZOL,OXFENDAZOL,INVERMECTIN are suggested. They are effective.
                            They need repeatation after three weeks. In case of coccidiosis sulpha drugs can be given.
                            There are ECTOPARASITIC DISEASES which infect skin. They are due to LICE/MITES. They cause
                            skin lesions. Loss of hair, irritation, suratching are the symptoms. Diagnosis of type of
                            mite can be carried out by examination of skin scrapings. Specific drugs are given for
                            specific infestation.</p>
