                        </h5>
                        <p class="mbr-text mbr-fonts-style display-4">It is the inflammation of the Udder. The causative
                            agent may be Virus, Bacteria,Fungus or Algae. Non infective cause is deficiency of
                            circulating cirtrate. Causative agent enters through teat sphincter (opening ) mostly after
                            milking (by hand or Machine) When sphincter muscles are yet to close tightly. (45 minutes to
                            1 ½ hour after milking). Once entered , it has good media to multiply inside the udder. In
                            per acute form animal may die. In Acute case there is high fever, udder is swollen and
                            painful. There is watery milk. In sub acute cases there is little sweating, no fever but
                            milk is not normal. In chronic cases, there is little abnormality in udder how-ever milk
                            quantity as well as quality is depressed. The economic losses due to sub-clinical mastitis
                            are more than clinical mastitis. In this form of disease there is no outward abnormality in
                            the udder.</p>
