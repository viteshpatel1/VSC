                            history. Treatment of calcium borogluconate 25%solution 200 to 400 ml Intravenously gives
                            quick results. Injection of ivt.D3 is also useful. Prevention can be carried out by anionic
                            mixtures before calving for calcium absorption. They are Ammonium chloride @150 gms/day for
                            10 days before calving.</p>
